"use client";

import { useEffect, useState } from "react";
import { useParams, useRouter } from "next/navigation";
import { useAuth } from "@clerk/nextjs";
import { Card, CardContent, CardHeader, CardTitle, CardDescription } from "@/components/ui/card";
import { Button } from "@/components/ui/button";
import { Loader2 } from "lucide-react";
import ReactMarkdown from "react-markdown";
import { RadialBarChart, RadialBar, ResponsiveContainer, PolarAngleAxis, Tooltip } from "recharts";

export default function EvaluationDashboard() {
  const params = useParams();
  const sessionId = params.sessionId as string;
  const router = useRouter();
  const { getToken, userId } = useAuth();
  
  const [evaluation, setEvaluation] = useState<any>(null);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState("");

  useEffect(() => {
    const fetchEvaluation = async () => {
      try {
        const token = await getToken();
        const res = await fetch(`http://localhost:5001/api/interviews/${sessionId}/evaluation?userId=${userId}`, {
          headers: {
            "Authorization": `Bearer ${token}`
          }
        });
        
        const data = await res.json();
        
        if (!res.ok) {
          throw new Error(data.error || "Failed to fetch evaluation");
        }
        
        setEvaluation(data);
      } catch (err: any) {
        setError(err.message);
      } finally {
        setLoading(false);
      }
    };

    if (userId && sessionId) {
      fetchEvaluation();
    }
  }, [sessionId, userId, getToken]);

  if (loading) {
    return (
      <div className="flex items-center justify-center min-h-[80vh]">
        <Loader2 className="h-10 w-10 animate-spin text-primary" />
        <p className="ml-4 text-lg">Fetching your AI evaluation...</p>
      </div>
    );
  }

  if (error || !evaluation) {
    return (
      <div className="flex flex-col items-center justify-center min-h-[80vh]">
        <p className="text-red-500 mb-4">{error || "Evaluation not found"}</p>
        <Button onClick={() => router.push("/dashboard")}>Back to Dashboard</Button>
      </div>
    );
  }

  const chartData = [
    { name: "Technical", score: evaluation.technicalScore, fill: "hsl(var(--chart-1))" },
    { name: "Communication", score: evaluation.communicationScore, fill: "hsl(var(--chart-2))" },
    { name: "Completeness", score: evaluation.completenessScore, fill: "hsl(var(--chart-3))" },
    { name: "Overall", score: evaluation.overallScore, fill: "hsl(var(--chart-4))" }
  ];

  return (
    <div className="container mx-auto py-10 max-w-5xl space-y-8 animate-in fade-in duration-500">
      
      <div className="flex items-center justify-between">
        <div>
          <h1 className="text-4xl font-bold tracking-tight">Your Evaluation Results</h1>
          <p className="text-muted-foreground mt-2 text-lg">Here is how you performed in your recent mock interview.</p>
        </div>
        <Button onClick={() => router.push("/dashboard")} variant="outline">
          Back to Dashboard
        </Button>
      </div>

      <div className="grid grid-cols-1 md:grid-cols-4 gap-6">
        {chartData.map((data, index) => (
          <Card key={index} className="flex flex-col items-center justify-center p-6 text-center shadow-sm">
            <CardHeader className="pb-2">
              <CardTitle className="text-lg text-muted-foreground">{data.name}</CardTitle>
            </CardHeader>
            <CardContent>
              <div className="h-32 w-32 relative">
                <ResponsiveContainer width="100%" height="100%">
                  <RadialBarChart 
                    innerRadius="80%" 
                    outerRadius="100%" 
                    data={[data]} 
                    startAngle={90} 
                    endAngle={-270}
                  >
                    <PolarAngleAxis type="number" domain={[0, 100]} angleAxisId={0} tick={false} />
                    <RadialBar
                      background
                      dataKey="score"
                      cornerRadius={10}
                    />
                  </RadialBarChart>
                </ResponsiveContainer>
                <div className="absolute inset-0 flex items-center justify-center flex-col">
                  <span className="text-3xl font-bold">{data.score}</span>
                  <span className="text-xs text-muted-foreground">/ 100</span>
                </div>
              </div>
            </CardContent>
          </Card>
        ))}
      </div>

      <Card className="shadow-sm">
        <CardHeader>
          <CardTitle className="text-2xl">Detailed AI Feedback</CardTitle>
          <CardDescription>Generated by an expert technical interviewer</CardDescription>
        </CardHeader>
        <CardContent>
          <div className="prose prose-neutral dark:prose-invert max-w-none">
            <ReactMarkdown>{evaluation.feedback}</ReactMarkdown>
          </div>
        </CardContent>
      </Card>

    </div>
  );
}
